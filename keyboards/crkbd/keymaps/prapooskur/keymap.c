// OLED TIMEOUT CODE STARTS HERE
static uint16_t oled_timer = 0;

//on key press do these things
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed) {
        oled_timer = timer_read();
        if (!is_oled_on())
        {
            oled_on();
        }
    }
    return true;
}

bool oled_task_user(void) {
    if (oled_timer == 0)
    {
        oled_timer = timer_read();
    }
    if (is_keyboard_master()) {
        oled_render_layer_state();
    } else {
        if (is_oled_on() && (timer_elapsed(oled_timer) < OLED_TIMEOUT)) {
            oled_render_space();
        }
        else if (is_oled_on()) {
            oled_off();
        }
    }
    return false;
}
