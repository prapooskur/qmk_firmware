#define L_BASE 0
#define L_LOWER 2
#define L_RAISE 4
#define L_ADJUST 8

void oled_render_layer_state(void) {
    oled_write_ln_P(PSTR(""), false);
    oled_write_ln_P(PSTR(""), false);
//    oled_write_P(PSTR("Layer: "), false);
    oled_write_P(PSTR("   "), false);
    switch (layer_state) {
        case L_BASE:
            oled_write_ln_P(PSTR("     -----"), false);
            break;
        case L_LOWER:
            oled_write_ln_P(PSTR("<<<<<-----"), false);
            break;
        case L_RAISE:
            oled_write_ln_P(PSTR("     ----->>>>>"), false);
            break;
        case L_ADJUST:
        case L_ADJUST|L_LOWER:
        case L_ADJUST|L_RAISE:
        case L_ADJUST|L_LOWER|L_RAISE:
            oled_write_ln_P(PSTR("<<<<<----->>>>>"), false);
            break;
    }
}

